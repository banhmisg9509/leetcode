// @lc code=start
/**
 * @param {string} s
 * @return {boolean}
 */
var isPalindrome = function(s) {
    s = s.replace(/[^a-zA-Z0-9]/g, '').toLocaleLowerCase().split('');
    return s.join('') === s.reverse().join('');
};
